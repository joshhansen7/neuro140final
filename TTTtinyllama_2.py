# Function to format tweets for training
def format_for_training(examples):
    texts = examples['text']
    # Format as instruction-tuning format for better control
    formatted_texts = [
        f"Generate a tweet in Donald Trump's style: {text}"
        for text in texts
    ]
    return formatted_texts

